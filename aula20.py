p1 = input('Digite um valor: ')
p2 = input('Digite outro valor: ')

if p1 >= p2:
    print(
        'O primeiro valor = {} é maior ou igual ao segundo valor = {}'
        .format(p1, p2)
    )
else:
    print('O segundo valor = {} é maior que o primeiro valor = {}'.format(p2, p1))

