# Variaveis sao usadas para salvar algo na memoria do computador.
# PEP8: inicie variaveis com letras minusculas, pode usar numeros e underline _.
# O sinal de = é o operador de atribuiçao.
# Ele é usado para atribuir um valor a um nome (variável).
# Uso: nome_variavel = expressão

nome = 'Taylan'
idade = 45
maior_de_idade = idade >= 18
print('Nome: ', nome)
print('Idade: ',idade)
print(nome, 'é maior de idade? ', maior_de_idade)
