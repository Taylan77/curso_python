# Conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o fato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int , float, bool 


print(1 + 1) # -> Somando 
print('a' + 'b') # -> Concatenando
# print('1' + 1) -> Nao se pode somar STR com INT 
# (Valido para todos tipos de dados)  

#Convertendo tipos de dados
print(int('1'), type(int('1')))
print(float('1') + 1)
print(bool(''))
print(str(11) + 'b')
